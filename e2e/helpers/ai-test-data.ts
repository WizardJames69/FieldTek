// ── Embedding Dimension + Deterministic Fallback ────────────────
// Real embeddings for the chunk texts below live in the checked-in fixture
// e2e/fixtures/chunk-embeddings.json (generated by
// scripts/generate-e2e-embeddings.ts with text-embedding-3-small) and are
// loaded at seed time by ai-seed-helpers. The function embeds user queries
// with the same model at request time, so real fixture vectors produce real,
// above-threshold matches — unlike the previous hand-tuned synthetic vectors,
// which scored marginally and tripped the grounded-retrieval abstain gate.
//
// `fallbackEmbedding` is used ONLY when a chunk text has no fixture entry
// (e.g. a text was edited without regenerating). It yields a deterministic,
// normalized pseudo-vector so seeding never crashes — but it will NOT match
// real query embeddings, so the dependent grounded test fails loudly (and the
// seed helper logs a warning) instead of silently passing on garbage.

export const EMBEDDING_DIMENSION = 1536;

export function fallbackEmbedding(text: string): number[] {
  const vec = new Array(EMBEDDING_DIMENSION).fill(0);
  // Seed the first 16 dims from a deterministic FNV-1a hash of the text.
  let h = 2166136261;
  for (let i = 0; i < text.length; i++) {
    h ^= text.charCodeAt(i);
    h = Math.imul(h, 16777619);
    vec[i % 16] += ((h >>> 0) % 1000) / 1000;
  }
  const norm = Math.sqrt(vec.reduce((s, v) => s + v * v, 0));
  return norm > 0 ? vec.map((v) => v / norm) : vec;
}

