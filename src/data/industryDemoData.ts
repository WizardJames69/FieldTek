// Helper to get relative dates - now always uses current date
const getDate = (daysOffset: number): string => {
  const today = new Date(); // Fresh date each call
  const date = new Date(today);
  date.setDate(date.getDate() + daysOffset);
  return date.toISOString().split('T')[0];
};

// Helper to get ISO timestamp with offset
const getTimestamp = (daysOffset: number): string => {
  const today = new Date();
  const date = new Date(today);
  date.setDate(date.getDate() + daysOffset);
  return date.toISOString();
};

