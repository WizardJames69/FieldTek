export function getDemoDataForIndustry(industry: IndustryType): DemoDataSet {
  switch (industry) {
    case 'hvac':
      return createHvacDemoData();
    case 'plumbing':
      return createPlumbingDemoData();
    case 'electrical':
      return createElectricalDemoData();
    case 'elevator':
      return createElevatorDemoData();
    case 'home_automation':
      return createHomeAutomationDemoData();
    case 'mechanical':
    case 'general':
    default:
      return createGeneralDemoData();
  }
}

